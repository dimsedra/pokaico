export const ShroomSprite: React.FC<ShroomSpriteProps> = ({
  expression,
  size = 120,
  className = ''
}) => {
  const pixelMap = PIXEL_ART_MAPS[expression] || PIXEL_ART_MAPS.idle;

  return (
    <div 
      className={`relative inline-block select-none ${className}`} 
      style={{ width: size, height: size }}
    >
      <svg
        viewBox="0 0 16 16"
        width="100%"
        height="100%"
        xmlns="http://www.w3.org/2000/svg"
        shapeRendering="crispEdges"
        className={`w-full h-full ${expression === 'loading' ? 'animate-shroom-pulse' : 'animate-shroom-bob'}`}
      >
        {pixelMap.map((row, y) =>
          row.map((pixel, x) => {
            const fillColor = COLOR_MAP[pixel] || 'transparent';
            if (fillColor === 'transparent') return null;
            return (
              <rect
                key={`${x}-${y}`}
                x={x}
                y={y}
                width="1"
                height="1"
                fill={fillColor}
              />
            );
          })
        )}
      </svg>
    </div>
  );
};
